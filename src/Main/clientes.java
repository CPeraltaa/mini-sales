    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();        
        model.setRowCount(0);
        for (int i = 0; i < client.size(); i++) {
            String nombre = client.get(i).nombre;
            String codigo = client.get(i).codigo;
            String nit = client.get(i).nit;
            String dir = client.get(i).direccion;            
            Object data[] = {codigo, nombre, nit, dir};
            model.addRow(data);
        }
    }
    
    private void clearFields(){
        txtCodigo.setText("");
        txtDireccion.setText("");
        txtNit.setText("");
        txtNombre.setText("");
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        for (Cliente cli : client) {
            if (cli.codigo.equals(txtCodigo.getText())){
                JOptionPane.showMessageDialog(this, "El codigo de cliente ya fue registrado previamente.","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        Cliente temp = new Cliente(txtCodigo.getText(), txtNombre.getText(), txtNit.getText(), txtDireccion.getText());
        client.add(temp);
        populateTable();
        clearFields();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String code = model.getValueAt(jTable1.getSelectedRow(), 0).toString();
        String name = model.getValueAt(jTable1.getSelectedRow(), 1).toString();
        String nit = model.getValueAt(jTable1.getSelectedRow(), 2).toString();
        String dir = model.getValueAt(jTable1.getSelectedRow(), 3).toString();        
        
        txtNombre.setText(name);
        txtCodigo.setText(code);
        txtNit.setText(nit);
        txtDireccion.setText(dir);        
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        for (Cliente cli : client) {
            if (cli.codigo.equals(txtCodigo.getText())){
                cli.nombre = this.txtNombre.getText();
                cli.nit = this.txtNit.getText();
                cli.direccion = txtDireccion.getText();
             JOptionPane.showMessageDialog(this, "El cliente con codigo " + this.txtCodigo.getText() + " fue actualizado."
                     ,"Exito en actualizacion", JOptionPane.INFORMATION_MESSAGE);
             populateTable();
             return;
            }
        }
        JOptionPane.showMessageDialog(this, "El codigo de cliente no fue encontrado.","Error", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        for (Cliente cli : client) {
            if (cli.codigo.equals(txtCodigo.getText())){
                client.remove(cli);
             JOptionPane.showMessageDialog(this, "El cliente con codigo " + this.txtCodigo.getText() + " fue eliminado."
                     ,"Exito en eliminacion", JOptionPane.INFORMATION_MESSAGE);
             populateTable();
             clearFields();
             return;
            }
        }
        JOptionPane.showMessageDialog(this, "El codigo de cliente no fue encontrado.","Error", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton3ActionPerformed


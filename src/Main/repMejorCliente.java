    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        temporal = new ArrayList<>();
        for (Factura f : listFact) {
            temporal.add(f.client.nit);
        }
        Map<String, Long> couterMap = temporal.stream().collect(Collectors.groupingBy(e -> e, Collectors.counting()));
        Map.Entry<String, Long> tempo = couterMap.entrySet().stream().sorted(reverseOrder(Map.Entry.comparingByValue())).findFirst().get();
        String nit = tempo.getKey();
        txtNit.setText(nit);
        int contador = 0;
        double monto = 0;
        Cliente cl = null;
        for (Factura f : listFact) {
            if (f.client.nit.equals(nit)) {
                cl = f.client;
                contador++;
                monto += f.total;
            }
        }
        if (cl != null) {
            txtNombre.setText(cl.nombre);
            txtCodigo.setText(cl.codigo);
            txtMonto.setText(String.valueOf(monto));
            txtCompras.setText(String.valueOf(contador));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

